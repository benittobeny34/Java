
public class primitive_data_types {
    public static void main(String arr[]){
        int i=56655;//2^32 to 2^32-1
        char c='G';
        short s=32767;//range between -32768 to 32767
        double d=335354355.355;
        float f=55455.5f;
        long l=254544233;//upto 9 digit
        System.out.println("The interger: "+i);
        System.out.println("The character: "+c);
        System.out.println("The short: "+s);
        System.out.println("The double: "+d);
        System.out.println("The float: "+f);
        System.out.println("The long: "+l);
        
    }
    
}
