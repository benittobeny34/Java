public class switch_case_faq {
    public static void main(String arr[]){
        int ch=97;
        switch(ch){
            case 'a':System.out.println("You entered the ascii value of a:");break;
            case 'b':System.out.println("You entered the ascii value of b:");break;
            case 'c':System.out.println("You entered the ascii value of c:");break;
            default :System.out.println("nothing valid statement:");break;
        }
    }    
}
